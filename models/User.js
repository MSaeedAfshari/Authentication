const mongoose = require("mongoose");

const schema = mongoose.Schema({
  firstname: {
    type: String,
    required: true,
  },
  lastname: {
    type: String,
    required: true,
  },
  username: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    required: true,
  },
});

const userModel = mongoose.model.User
  ? mongoose.model.User
  : mongoose.model("User", schema);

export default userModel;
